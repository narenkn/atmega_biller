void
TftDrawCircle(uint16_t x0,uint16_t y0,uint8_t r)
{
  int a,b;
  int di;
  a=0;b=r;	
  di=3-(r<<1);
  while(a<=b) {
    TftDrawPoint(x0-b,y0-a);
    TftDrawPoint(x0+b,y0-a);
    TftDrawPoint(x0-a,y0+b);
    TftDrawPoint(x0-b,y0-a);
    TftDrawPoint(x0-a,y0-b);
    TftDrawPoint(x0+b,y0+a);
    TftDrawPoint(x0+a,y0-b);
    TftDrawPoint(x0+a,y0+b);
    TftDrawPoint(x0-b,y0+a);
    a++;
		
    if(di<0)di +=4*a+6;	
    else {
      di+=10+4*(a-b);
      b--;
    }
    TftDrawPoint(x0+a,y0+b);
  }
}

void
TftShowChar(uint16_t x,uint16_t y,uint8_t num,uint8_t mode)
{
  uint8_t temp;
  uint8_t pos,t;
  uint16_t x0=x;
  uint16_t colortemp=POINT_COLOR;
  if(x>TFT_WIDTH-16||y>TFT_HEIGHT-16)return;	
	
  num=num-' ';
  TftAddressSet(x,y,x+8-1,y+16-1);
  if(!mode) {
    for(pos=0;pos<16;pos++) {
      temp=pgm_read_byte(&asc2_1608[(uint16_t)num*16+pos]);		
      for(t=0;t<8;t++) {
	if(temp&0x01)POINT_COLOR=colortemp;
	else POINT_COLOR=BACK_COLOR;
	TFT_WR_DATA(POINT_COLOR);	
	temp>>=1;
	x++;
      }
      x=x0;
      y++;
    }	
  } else {
    for(pos=0;pos<16;pos++) {
      temp=pgm_read_byte(&asc2_1608[(uint16_t)num*16+pos]);		
      for(t=0;t<8;t++) {
	if(temp&0x01)TftDrawPoint(x+t,y+pos);
	temp>>=1;
      }
    }
  }
  POINT_COLOR=colortemp;	    	   	 	
}

uint32_t
mypow(uint8_t m,uint8_t n)
{
  uint32_t result=1;	
  while(n--)result*=m;
  return result;
}			

void
TftShowNum(uint16_t x,uint16_t y,uint32_t num,uint8_t len)
{         	
  uint8_t t,temp;
  uint8_t enshow=0;
  num=(uint16_t)num;
  for(t=0;t<len;t++) {
    temp=(num/mypow(10,len-t-1))%10;
    if(enshow==0&&t<(len-1)) {
      if(temp==0) {
	TftShowChar(x+8*t,y,' ',0);
	continue;
      }else enshow=1;
		 	
    }
    TftShowChar(x+8*t,y,temp+48,0);
  }
}

void
TftShow2Num(uint16_t x,uint16_t y,uint16_t num,uint8_t len)
{         	
  uint8_t t,temp;						
  for(t=0;t<len;t++) {
    temp=(num/mypow(10,len-t-1))%10;
    TftShowChar(x+8*t,y,temp+'0',0);
  }
}

void
TftShowString(uint16_t x,uint16_t y,const uint8_t *p)
{
  while(*p!='\0') {
    if(x>TFT_WIDTH-16){x=0;y+=16;}
    if(y>TFT_HEIGHT-16){y=x=0;}
    TftShowChar(x,y,*p,0);
    x+=8;
    p++;
  }
}
